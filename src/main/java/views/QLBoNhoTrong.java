    public void loadMaBNT() {

        String ma = "";
        List<BoNhoTrong> bnts = services.getALL();
        if (bnts.size() == 0) {
            ma = "BN0";
        } else {
            BoNhoTrong sp = services.layMa();
//          
            ma = sp.getMaBNT();
        }

        String mangString[] = ma.split("");
        String so = "";
        for (int i = 2; i < mangString.length; i++) {
            so += mangString[i];
        }

        Integer sofinal = Integer.parseInt(so) + 1;
        String maMoi = "BN" + sofinal;
        txtMa.setText(maMoi);
    }

    public void load() {
        loadMaBNT();
        DefaultTableModel model = (DefaultTableModel) tbBNT.getModel();
        model.setRowCount(0);
        List<BoNhoTrong> list = services.getALL();
        for (BoNhoTrong m : list) {
            if (m.getTrangThai() == 0) {
                Object[] data = new Object[]{
                    m.getMaBNT(),
                    m.getDungLuong(),};
                model.addRow(data);
            }
        }
    }

    public void loadCbbBoNhoTrong() {
        List<BoNhoTrong> items = services.getALL();
        QLSanPham.loadCbbBoNhoo(items);
    }

    public BoNhoTrong layTT() throws ParseException {
        String ma = txtMa.getText();
        Integer dl = Integer.parseInt(txtTen.getText());
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("MM-dd-yyyy");
        ZonedDateTime now = ZonedDateTime.now();
        String ngayTao = dtf.format(now);
        Date date = new SimpleDateFormat("MM-dd-yyyy").parse(ngayTao);
        BoNhoTrong n = new BoNhoTrong();
        n.setMaBNT(ma);
        n.setDungLuong(dl);
        n.setNgayTao(date);
        n.setTrangThai(0);
        return n;
    }

