    private void btnUpdateMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMousePressed
        // TODO add your handling code here:
        String checkSo = "^[0-9]*$";
        try {
            // TODO add your handling code here:
            int index = tbBNT.getSelectedRow();
            if (index == -1) {
                JOptionPane.showMessageDialog(this, "Bạn chưa chọn bản ghi nào !");
                return;
            }
            int i = tbBNT.getSelectedRowCount();
            if (i > 1) {
                JOptionPane.showMessageDialog(this, "Bạn chỉ được chọn 1 bản ghi !");
                return;
            }

            if (txtMa.getText().trim().isEmpty() || txtTen.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không được để trống !");
                return;
            }
            if (!txtTen.getText().matches(checkSo)) {
                JOptionPane.showMessageDialog(this, "Dung lượng phải là số !");
                return;
            }
            if (txtTen.getText().length() > 4) {
                JOptionPane.showMessageDialog(this, "Dung lượng phải nhỏ hơn hoặc bằng 9999");
                return;
            }

            Integer dungLuong = Integer.parseInt(txtTen.getText());
            if (dungLuong <= 0) {
                JOptionPane.showMessageDialog(this, "Dung lượng phải lớn hơn 0 !");
                return;
            }
            int check = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn sửa ?");
            if (check != JOptionPane.YES_OPTION) {
                return;
            }
            BoNhoTrong n = layTTSua();
            if (services.update(n) == true) {
                JOptionPane.showMessageDialog(this, "Sửa thành công !");
                load();
                loadCbbBoNhoTrong();
            } else {
                JOptionPane.showMessageDialog(this, "Sửa thất bại !");
            }
        } catch (ParseException ex) {
            Logger.getLogger(QLBoNhoTrong.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateMousePressed

